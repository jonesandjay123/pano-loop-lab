import { readdir, readFile, writeFile } from "node:fs/promises";
import { existsSync } from "node:fs";
import path from "node:path";
import process from "node:process";

const REPO_ROOT = process.cwd();
const CANDIDATE_ROOT = path.join(REPO_ROOT, "public", "panos", "adapter-candidates");
const OUTPUT_PATH = path.join(REPO_ROOT, "src", "pano", "adapterCandidates.generated.ts");

function repoRelative(filePath) {
  return path.relative(REPO_ROOT, filePath).split(path.sep).join("/");
}

function publicUrlFromPath(filePath) {
  const publicPrefix = `${path.join(REPO_ROOT, "public")}${path.sep}`;
  if (!filePath.startsWith(publicPrefix)) {
    throw new Error(`Candidate path must be under public/: ${repoRelative(filePath)}`);
  }
  return `/${path.relative(path.join(REPO_ROOT, "public"), filePath).split(path.sep).join("/")}`;
}

async function readCandidateGroups() {
  if (!existsSync(CANDIDATE_ROOT)) {
    return {};
  }

  const groups = {};
  const entries = await readdir(CANDIDATE_ROOT, { withFileTypes: true });
  for (const entry of entries) {
    if (!entry.isDirectory()) {
      continue;
    }

    const pairKey = entry.name;
    const jsonPath = path.join(CANDIDATE_ROOT, pairKey, "candidates.json");
    if (!existsSync(jsonPath)) {
      continue;
    }

    const data = JSON.parse(await readFile(jsonPath, "utf8"));
    groups[pairKey] = {
      activeForReview: data.activeForReview ?? null,
      candidates: (data.candidates ?? []).map((candidate) => ({
        id: candidate.id,
        label: candidate.label ?? candidate.id,
        imageUrl: publicUrlFromPath(path.join(REPO_ROOT, candidate.localPath)),
        status: candidate.status ?? "generated",
        notes: candidate.reviewNotes ?? candidate.notes ?? "",
        reviewSummary: candidate.reviewSummary ?? null,
      })),
    };
  }

  return groups;
}

async function main() {
  const groups = await readCandidateGroups();
  const body = `// Generated by scripts/adapter-candidates-generate.mjs. Do not edit by hand.\n` +
    `export const ADAPTER_CANDIDATES_BY_PAIR = ${JSON.stringify(groups, null, 2)} as const;\n`;
  await writeFile(OUTPUT_PATH, body);
  console.log(`Wrote ${repoRelative(OUTPUT_PATH)}`);
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exitCode = 1;
});
